export function parseCountriesToPoints(
  countriesStr: string,
  tripId: string,
  tripName: string,
  color: string
) {
  if (!countriesStr) return [];

  const names = countriesStr
    .split(/[,，、\/]/)
    .map((s) => s.trim())
    .filter(Boolean);

  const points: {
    lat: number;
    lng: number;
    tripId: string;
    tripName: string;
    country: string;
    color: string;
  }[] = [];

  for (const name of names) {
    const coords = COUNTRY_COORDS[name] ?? COUNTRY_COORDS[name.toLowerCase()];
    if (coords) {
      points.push({ ...coords, tripId, tripName, country: name, color });
    }
  }

  return points;
}

