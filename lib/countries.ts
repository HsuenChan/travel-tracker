function flagEmoji(code: string): string {
  const offset = 0x1f1e6 - 65;
  return [...code.toUpperCase()]
    .map((c) => String.fromCodePoint(c.charCodeAt(0) + offset))
    .join("");
}

export function getCountryFlags(countriesStr: string): string {
  if (!countriesStr) return "";
  return countriesStr
    .split(/[,，、\/]/)
    .map((s) => s.trim())
    .filter(Boolean)
    .slice(0, 4)
    .map((name) => {
      const code = COUNTRY_CODES[name] ?? COUNTRY_CODES[name.toLowerCase()];
      return code ? flagEmoji(code) : null;
    })
    .filter(Boolean)
    .join(" ");
}

