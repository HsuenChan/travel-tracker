export function parseTripArcs(
  countriesStr: string,
  tripId: string,
  color: string
) {
  if (!countriesStr) return [];

  const names = countriesStr
    .split(/[,，、\/]/)
    .map((s) => s.trim())
    .filter(Boolean);

  const arcs: {
    startLat: number;
    startLng: number;
    endLat: number;
    endLng: number;
    tripId: string;
    color: string;
  }[] = [];

  for (let i = 0; i < names.length - 1; i++) {
    const start = COUNTRY_COORDS[names[i]] ?? COUNTRY_COORDS[names[i].toLowerCase()];
    const end = COUNTRY_COORDS[names[i + 1]] ?? COUNTRY_COORDS[names[i + 1].toLowerCase()];
    if (start && end) {
      arcs.push({
        startLat: start.lat,
        startLng: start.lng,
        endLat: end.lat,
        endLng: end.lng,
        tripId,
        color,
      });
    }
  }

  return arcs;
}
